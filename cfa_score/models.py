# FieldPolicy (extended with semantic aliases and LLM extraction config).
# ---------------------------------------------------------------------------

@dataclass(frozen=True)
class FieldPolicy:
    # ---- 核心字段分类 ----
    protected_fields: List[str]
    identifier_fields: List[str]
    quasi_identifier_fields: List[str]
    field_order: List[str]
    field_labels: Dict[str, str]
    field_weights: Dict[str, float]
    field_aliases: Dict[str, Dict[str, List[str]]]
    public_rules: List[Dict[str, Any]]

    # ---- 唯一性阈值 ----
    uniqueness_k: int = 1
    display_field: str = "system_name"

    # ---- 安全改写配置 ----
    safe_replacements: Dict[str, str] = field(default_factory=dict)
    safe_hint: str = ""

    # ---- 显式敏感字段池（额外补充，可与 protected/id/display 自动合并） ----
    sensitive_fields: List[str] = field(default_factory=list)

    # ---- v2.3 间接还原检测配置 ----
    indirect_restoration_enabled: bool = True
    min_candidate_reduction: int = 1
    min_information_gain_bits: float = 0.5
    protected_value_k: int = 1
    min_protected_entropy_drop_bits: float = 0.5

    # ---- LLM 相关配置 ----
    semantic_aliases: Dict[str, Dict[str, Dict[str, Any]]] = field(default_factory=dict)
    match_type_weights: Dict[str, float] = field(default_factory=dict)
    llm_extraction_fields: List[str] = field(default_factory=list)
    llm_confidence_threshold: float = 0.4
    llm_max_accepted_values: int = 30

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "FieldPolicy":
        semantic_raw = data.get("semantic_aliases", {})
        semantic_aliases: Dict[str, Dict[str, Dict[str, Any]]] = {}
        if isinstance(semantic_raw, dict):
            for field_name, value_map in semantic_raw.items():
                if isinstance(value_map, dict):
                    semantic_aliases[str(field_name)] = {
                        str(canonical): dict(info) if isinstance(info, dict) else {}
                        for canonical, info in value_map.items()
                    }

        return cls(
            protected_fields=list(data.get("protected_fields", [])),
            identifier_fields=list(data.get("identifier_fields", [])),
            quasi_identifier_fields=list(data.get("quasi_identifier_fields", [])),
            field_order=list(data.get("field_order", [])),
            field_labels=dict(data.get("field_labels", {})),
            field_weights=dict(data.get("field_weights", {})),
            field_aliases=dict(data.get("field_aliases", {})),
            public_rules=list(data.get("public_rules", [])),
            uniqueness_k=int(data.get("uniqueness_k", 1)),
            display_field=str(data.get("display_field", "system_name")),
            safe_replacements=dict(data.get("safe_replacements", {})),
            safe_hint=str(data.get("safe_hint", "")),
            sensitive_fields=list(data.get("sensitive_fields", [])),
            # v2.3
            indirect_restoration_enabled=bool(data.get("indirect_restoration_enabled", True)),
            min_candidate_reduction=int(data.get("min_candidate_reduction", 1)),
            min_information_gain_bits=float(data.get("min_information_gain_bits", 0.5)),
            protected_value_k=int(data.get("protected_value_k", 1)),
            min_protected_entropy_drop_bits=float(data.get("min_protected_entropy_drop_bits", 0.5)),
            # LLM
            semantic_aliases=semantic_aliases,
            match_type_weights=dict(data.get("match_type_weights", {})),
            llm_extraction_fields=list(data.get("llm_extraction_fields", [])),
            llm_confidence_threshold=float(data.get("llm_confidence_threshold", 0.4)),
            llm_max_accepted_values=int(data.get("llm_max_accepted_values", 30)),
        )

    def label(self, field_name: str) -> str:
        return self.field_labels.get(field_name, field_name)

    def weight(self, field_name: str) -> float:
        return float(self.field_weights.get(field_name, 0.05))

    def match_type_weight(self, match_type: str) -> float:
        """Get the weight multiplier for a given match type."""
        if self.match_type_weights:
            return float(self.match_type_weights.get(match_type, 1.0))
        return DEFAULT_MATCH_TYPE_WEIGHTS.get(match_type, 1.0)

    def get_semantic_aliases(self, field_name: str) -> Dict[str, SemanticFieldAlias]:
        """Return parsed semantic aliases for a field."""
        raw = self.semantic_aliases.get(field_name, {})
        result: Dict[str, SemanticFieldAlias] = {}
        for canonical, info in raw.items():
            result[canonical] = SemanticFieldAlias(
                components=[str(c) for c in info.get("components", [])],
                aliases=[str(a) for a in info.get("aliases", [])],
                partial_clues=[str(p) for p in info.get("partial_clues", [])],
                possible_inferences=[
                    dict(i) for i in info.get("possible_inferences", [])
                ],
                partial_match_policy=str(
                    info.get("partial_match_policy", "any_component_or_alias")
                ),
            )
        return result


