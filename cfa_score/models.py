# Anchor (extended with LLM semantic fields).
# ---------------------------------------------------------------------------

@dataclass
class Anchor:
    id: str
    field_name: str
    field_label: str
    text: str
    canonical_value: str
    start: int
    end: int
    anchor_type: str
    protected: bool
    inferred: bool = False
    evidence: str = ""
    source_anchor_id: Optional[str] = None
    source: str = "output"

    # -------- New: LLM semantic extraction fields --------
    match_type: str = "exact"
    confidence: float = 1.0
    llm_reason: str = ""
    accepted_values: List[str] = field(default_factory=list)

    # -------- v2.3: Anchor provenance tracking --------
    extractor_sources: List[str] = field(default_factory=list)
    merged_anchor_ids: List[str] = field(default_factory=list)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "id": self.id,
            "field_name": self.field_name,
            "field_label": self.field_label,
            "text": self.text,
            "canonical_value": self.canonical_value,
            "start": self.start,
            "end": self.end,
            "anchor_type": self.anchor_type,
            "protected": self.protected,
            "source": self.source,
            "inferred": self.inferred,
            "evidence": self.evidence,
            "source_anchor_id": self.source_anchor_id,
            "match_type": self.match_type,
            "confidence": self.confidence,
            "llm_reason": self.llm_reason,
            "accepted_values": list(self.accepted_values),
            "extractor_sources": list(self.extractor_sources),
            "merged_anchor_ids": list(self.merged_anchor_ids),
        }

    def effective_canonical_value(self) -> str:
        """Return the best representative value for display."""
        if self.canonical_value:
            return self.canonical_value
        if self.accepted_values:
            if len(self.accepted_values) == 1:
                return self.accepted_values[0]
            return f"{{{len(self.accepted_values)}个候选}}"
        return ""

    def match_symbol(self) -> str:
        """Return the symbol to display in reduction chains."""
        if self.match_type in ("exact", "alias"):
            return "="
        return "≈"


