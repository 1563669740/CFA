@dataclass
class ReductionStep:
    field_name: str
    field_label: str
    anchor_text: str
    canonical_value: str
    before_count: int
    after_count: int
    remaining_asset_ids: List[str]
    match_symbol: str = "="

    def to_dict(self) -> Dict[str, Any]:
        return asdict(self)


@dataclass
class RiskFinding:
    target_asset_id: str
    target_asset_name: str
    risk_level: str
    score: float
    reason: str
    restored_fact: str
    anchors: List[Anchor]
    reduction_chain: List[ReductionStep]
    minimal_combinations: List[List[str]]
    key_anchor_ids: List[str] = field(default_factory=list)
    key_anchor_summary: List[str] = field(default_factory=list)

    # -------- v2.3 extensions --------
    finding_type: str = "asset_restoration"
    target_asset_ids: List[str] = field(default_factory=list)
    restored_fields: List[str] = field(default_factory=list)
    input_candidate_count: int = 0
    final_candidate_count: int = 0
    information_gain_bits: float = 0.0

    def to_dict(self) -> Dict[str, Any]:
        data = asdict(self)
        data["anchors"] = [a.to_dict() for a in self.anchors]
        data["reduction_chain"] = [s.to_dict() for s in self.reduction_chain]
        return data


@dataclass
class AnalysisResult:
    raw_answer: str
    anchors: List[Anchor]
    findings: List[RiskFinding]
    x_replaced_answer: str
    safe_answer: str
    user_input: str = ""
    model_output: str = ""
    secondary_check_performed: bool = False
    secondary_safe_answer: str = ""
    secondary_findings: List[RiskFinding] = field(default_factory=list)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "user_input": self.user_input,
            "model_output": self.model_output or self.raw_answer,
            "raw_answer": self.raw_answer,
            "anchors": [a.to_dict() for a in self.anchors],
            "findings": [f.to_dict() for f in self.findings],
            "x_replaced_answer": self.x_replaced_answer,
            "safe_answer": self.safe_answer,
            "secondary_check_performed": self.secondary_check_performed,
            "secondary_safe_answer": self.secondary_safe_answer,
            "secondary_findings": [f.to_dict() for f in self.secondary_findings],
        }